     * @return Shopware\Models\Category\Repository
     */
    protected function getRepo()
    {
        if ($this->repo === null) {
            $this->repo = Shopware()->Models()->Category();
        }
        return $this->repo;
    }

    public function testExpected1()
    {
        foreach ($this->expected1 as $id => $expected) {
            $data = $this->getRepo()->getActiveChildrenList($id);
            $data = $this->removeSimpleDates($data);
            $this->assertEquals($expected, $data);
        }
    }

    public function testExpected2()
    {
        foreach ($this->expected2 as $id => $expected) {
            $data = $this->getRepo()->getActiveChildrenList($id, 1, 2);
            $data = $this->removeSimpleDates($data);
            $this->assertEquals($expected, $data);
        }
    }

    protected function removeSimpleDates($data)
    {
        foreach ($data as &$subCategory) {
            unset($subCategory['changed']);
            unset($subCategory['cmsText']);
            unset($subCategory['added']);
            foreach ($subCategory['emotions'] as &$emotion) {
                unset($emotion['createDate']);
                unset($emotion['modified']);
            }
            foreach ($subCategory['articles'] as &$article) {
                unset($article['added']);
                unset($article['changed']);
                unset($article['description']);
                unset($article['descriptionLong']);
                unset($article['mainDetail']['releaseDate']);
            }
            if ($subCategory['sub']) {
                $subCategory['sub'] = $this->removeSimpleDates($subCategory['sub']);
            }
        }
        return $data;
    }
